// AND Operator &&
console.log(true && true);
console.log(true && false);
console.log('sentence' == 'sentence' && 5 == 5);
console.log(5 == 5 && 'sentence1' == 'sentence2');

// OR Operator ||
console.log(true || true);
console.log(false || false);
console.log('sentence' == 'sentence' || 5 == 10);

// NOT Operator !
console.log(!true);
console.log(!false);
console.log(!5);
console.log(!'sentence');
console.log(!!5);
console.log(!!'sentence');